
public abstract class Decorator {
    public Decorator() {
    }

    public Decorator(float price, String desc) {
        this.price = price;
        this.desc = desc;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    private float price;
    private String desc;

    public abstract float cost();

}
